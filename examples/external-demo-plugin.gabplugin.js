(function () {
  const host = window.GalAssetBoxPluginHost;
  if (!host) throw new Error("GalAssetBoxPluginHost is not available.");

  host.registerAuthorizedPlugin({
    id: "example.external-demo-authorized-pack",
    name: "外部插件示例：授权 JSON 包",
    description: "演示从外部 .gabplugin.js 文件导入插件，只处理 externaldemo.json 示例格式。",
    version: "0.1.0",
    author: "GalAssetBox demo",
    license: "MIT-0",
    enabled: true,
    defaultEnabled: true,
    capabilities: ["inspect", "authorized-transform", "external-demo"],
    policy: {
      requiresUserAuthorization: true,
      localOnly: true,
      noDrmBypass: true,
      noBundledThirdPartyKeys: true,
    },
    notes: "示例插件不包含第三方游戏规则、密钥、破解或 DRM 绕过逻辑。",
    match(record) {
      if (!record.path.toLowerCase().endsWith(".externaldemo.json")) return null;
      return {
        confidence: "high",
        label: "外部授权示例包",
        note: "可在用户确认后转换成文本文件。",
      };
    },
    async transform({ readText }) {
      const source = JSON.parse(await readText());
      if (!source || source.format !== "GalAssetBox.externalDemo.v1") {
        throw new Error("不是 GalAssetBox 外部插件示例格式。");
      }
      return [
        {
          path: "external-demo/summary.txt",
          type: "text/plain;charset=utf-8",
          text: [
            `Title: ${source.title || "Untitled"}`,
            `Message: ${source.message || ""}`,
            "Generated by external demo plugin.",
          ].join("\n"),
        },
      ];
    },
  });
})();
