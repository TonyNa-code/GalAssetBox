(function () {
  const host = window.GalAssetBoxPluginHost;
  if (!host) throw new Error("GalAssetBoxPluginHost is not available.");

  host.registerAuthorizedPlugin({
    id: "example.package-demo-authorized-pack",
    name: "插件包示例：授权 JSON 包",
    description: "从插件包目录导入的示例插件，只处理 packagepack.json 示例格式。",
    version: "0.1.0",
    author: "GalAssetBox demo",
    license: "MIT-0",
    enabled: true,
    defaultEnabled: true,
    capabilities: ["inspect", "authorized-transform", "package-demo"],
    policy: {
      requiresUserAuthorization: true,
      localOnly: true,
      noDrmBypass: true,
      noBundledThirdPartyKeys: true,
    },
    notes: "示例插件包不包含第三方游戏规则、密钥、破解或 DRM 绕过逻辑。",
    match(record) {
      if (!record.path.toLowerCase().endsWith(".packagepack.json")) return null;
      return {
        confidence: "high",
        label: "插件包示例授权包",
        note: "可在用户确认后转换成摘要文本。",
      };
    },
    async transform({ readText }) {
      const source = JSON.parse(await readText());
      if (!source || source.format !== "GalAssetBox.packageDemo.v1") {
        throw new Error("不是 GalAssetBox 插件包示例格式。");
      }
      return [
        {
          path: "package-demo/summary.txt",
          type: "text/plain;charset=utf-8",
          text: [
            `Title: ${source.title || "Untitled"}`,
            `Items: ${Array.isArray(source.items) ? source.items.length : 0}`,
            "Generated by package demo plugin.",
          ].join("\n"),
        },
      ];
    },
  });
})();
